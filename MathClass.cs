using System;

class Program
{
    static void Main()
    {
        Console.WriteLine(Math.Abs(-10));       // 10
        Console.WriteLine(Math.Sqrt(16));       // 4
        Console.WriteLine(Math.Pow(3, 2));      // 9
        Console.WriteLine(Math.Max(5, 8));      // 8
        Console.WriteLine(Math.Round(7.6));     // 8
    }
}
